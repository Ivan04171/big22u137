from math import sqrt
print('введите число a')
a = int(input())
print('введите число b')
b = int(input())
print('введите число c')
c = int(input())
t1 = 0
t2 = 0
D = b ** 2 - 4 * a * c
if D < 0:
    print("Нет корней")
elif D == 0:
    t1 = -b / (2 * a)
    if t1 >= 0:
        print(sqrt(t1), -sqrt(t1))
    else:
        print("Нет корней")
elif D > 0:
    t1 = (-b + sqrt(D)) / (2 * a)
    t2 = (-b - sqrt(D)) / (2 * a)
    if t1 >= 0:
        print(sqrt(t1), -sqrt(t1))
    if t2 >= 0:
        print(sqrt(t2), -sqrt(t2))
    if t1 < 0 and t2 < 0:
        print("Нет корней")
